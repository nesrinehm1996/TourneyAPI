package com.tourney.api.viewmodels;

public class UserVM {
    String email;
    String password;
    String firstName;
    String lastName;
    int age;

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }
}
